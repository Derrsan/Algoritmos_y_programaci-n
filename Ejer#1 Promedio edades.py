# Pedir las edades al usuario
edad_persona_1 = int(input("Ingrese la edad de la persona 1: "))
edad_persona_2 = int(input("Ingrese la edad de la persona 2: "))
edad_persona_3 = int(input("Ingrese la edad de la persona 3: "))

# Calcular el promedio de las edades
promedio_edad = (edad_persona_1 + edad_persona_2 + edad_persona_3) / 3

# Mostrar el resultado
print("El promedio de edad es:", promedio_edad)
